function buildPapers() {
  let html = '';
  for (let i = 0; i < wallpapers.length; i++) {
    let w = wallpapers[i];
    html += "<tr><td width=202>";
    html += `<img src="images/${w.pathname}_thumb.jpg" lowsrc="images/please_wait.gif" width=200 height=160 border=1 alt="${w.title}">`;
    html += "</td><td valign=top>";
    html += "<h3>" + w.title + "</h3>";
    let resLinks = [];
    for (let r = 0; r < resolutions.length; r++) {
      res = resolutions[r]
      resLinks.push(`<a href="images/${w.pathname}_${res}.jpg" target=_blank>${res}</a>`);
    }
    html += "<p>" + resLinks.join("&nbsp;::&nbsp;") + "</p>";
    html += "</td></tr>";
  }
  return html;
}